package Uri._1_July;

import java.util.Scanner;

class FirstProgram {

    public static void main(String[] args) {

        int A, B, X;
        Scanner sc = new Scanner(System.in);
        A = sc.nextInt();
        B = sc.nextInt();

        X = A + B;
        System.out.println(X);
        X = A - B;
        System.out.println(X);
    }
}
